    private void btn_SelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SelectActionPerformed
        FileNameExtensionFilter pngFilter = new FileNameExtensionFilter("Select an Image", "png","jpg");
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(pngFilter);
        int aux = chooser.showOpenDialog(this);
        
        if (aux== JFileChooser.CANCEL_OPTION) {
            JOptionPane.showMessageDialog(null, "ADVISE: ACTION CANCELLED");
        } else {
            File fl = chooser.getSelectedFile();
            fr.cargarImagen(fl);
        }
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_btn_SelectActionPerformed

